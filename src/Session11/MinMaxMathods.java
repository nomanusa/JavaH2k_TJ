package Session11;

public class MinMaxMathods {

	public static void main(String[] args) {
		
		double a = 60.4536;
		double b = 35.5135;
		double c = 68.8536;
		double d = 10;
		double e = 15;
		
		// Min
		System.out.println(Math.min(a, b));
		System.out.println(Math.min(d, e));
		
		System.out.println(Math.min(9, 5)); // without initialize value
		
		// Max
		System.out.println();
		System.out.println(Math.max(a, b));
		System.out.println(Math.max(d, e));
		System.out.println(Math.max(c, d));
		
		// random
		System.out.println();
		System.out.println(Math.random()*10); // i have confusion
		
		
		
		

	}

}
